function [area_labels, area_cmap, area_match] = wang15_smry_atlas(idx)
% WANG15_LABEL outputs the atlas labels and respective colormap of
% wang15_atlas with summarization of the labels
% 
% Labels come from https://hub.docker.com/r/nben/occipital_atlas/
% 
% Wang L, et al. (2015) Probabilistic Maps of Visual Topography in Human Cortex, Cerebral Cortex.
% https://doi.org/10.1093/cercor/bhu277
% 
% See also WANG15_ATLAS

% K.Yuasa, BAIR 2022

area_labels =  {'V1','V2','V3', ...
                'hV4','VO','PHC', ...
                'TO','LO','V3AB', ...
                'IPS','SPL','FEF'}; 
            
area_cmap   = [255   0   0; 255 128   0; 255 255   0;
                 0 128 255;   0 102  51; 153 153   0;
               153  76   0; 102   0 204;   0 255   0;
               255 153 153; 255 153 255; 255 178 102]./255;
           
area_match = [1,1, ...
              2,2, ...
              3,3, ...
              4, ...
              5,5, ...
              6,6, ...
              7,7, ...
              8,8, ...
              9,9, ...
              10,10,10,10,10,10, ...
              11,12];

if exist('idx','var') && ~isempty(idx)
    area_labels = area_labels(idx);
    area_cmap   = area_cmap(idx,:);
end
